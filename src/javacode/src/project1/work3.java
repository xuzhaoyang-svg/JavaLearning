package javacode.src.project1;

public class work3 {
    public static void main(String args[]) {
        for(int m=1;m<=10;m++) {
            for(int n=10;n>0;n--) {
                if(n>m) {
                    System.out.print(" ");
                }else {
                    System.out.print("*");
                }
            }
            System.out.println();
        }
    }
}


